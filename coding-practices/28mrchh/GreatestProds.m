% The large number will be given as a string, 1xn characters.
% The name of your function should be GreatestProds

function y=GreatestProds(x)
n=length(x);
y=1;
for ii=1:n-4
    m=[str2num(x(ii))  str2num(x(ii+1)) str2num(x(ii+2)) str2num(x(ii+3)) str2num(x(ii+4))  ]
    y=max(y,prod(m));
    
end
end